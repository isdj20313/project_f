"""분석/발표용 예시 데이터 생성 모듈.

발표의 핵심은 "진짜 데이터는 벤포드를 따르고, 인위적으로 만든 데이터는 깨진다"를
눈으로 보여주는 것이다. 이 모듈은 그 대비를 만들기 위한 데이터를 제공한다.

외부 인터넷 연결 없이도 동작하도록, 통계적 성질이 알려진 데이터를
난수로 생성하거나 코드에 내장한다.
"""

from __future__ import annotations

import random


def benford_following_data(n: int = 1000, seed: int | None = 42) -> list[float]:
    """벤포드를 잘 따르는 데이터.

    여러 자릿수에 걸쳐 로그 스케일로 퍼지는 데이터는 벤포드를 따른다.
    여기서는 거듭제곱(지수) 분포를 이용해 그런 데이터를 만든다.
    (예: 자산, 인구, 거래액처럼 곱셈적으로 성장하는 값들의 모형)
    """
    rng = random.Random(seed)
    return [10 ** rng.uniform(0, 6) for _ in range(n)]


def uniform_random_data(n: int = 1000, low: int = 1, high: int = 1000,
                        seed: int | None = 7) -> list[float]:
    """균등 난수 데이터(벤포드가 깨지는 대표 예시).

    1~1000을 고르게 뽑으면 첫자리도 비교적 고르게 나와 벤포드와 다르다.
    """
    rng = random.Random(seed)
    return [rng.randint(low, high) for _ in range(n)]


def fabricated_data(n: int = 200, seed: int | None = 123) -> list[float]:
    """사람이 '그럴듯하게' 지어낸 느낌의 데이터.

    사람은 숫자를 꾸밀 때 무의식적으로 가운데 자리(3~7)나 특정 숫자를
    선호하는 경향이 있다. 이를 흉내 내어 첫자리가 한쪽으로 쏠리게 만든다.
    """
    rng = random.Random(seed)
    # 사람이 선호하기 쉬운 첫자리에 가중치를 둔다.
    weights = {1: 1, 2: 2, 3: 4, 4: 4, 5: 5, 6: 4, 7: 4, 8: 2, 9: 2}
    digits = list(weights.keys())
    probs = list(weights.values())
    data = []
    for _ in range(n):
        d = rng.choices(digits, weights=probs, k=1)[0]
        rest = rng.randint(0, 999)
        data.append(float(f"{d}{rest:03d}"))
    return data


